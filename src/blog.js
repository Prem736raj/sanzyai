// =============================================
// PAGINATION
// =============================================
function renderPagination() {
    const total = Math.ceil(displayPosts.length / PER_PAGE);
    const pg = document.getElementById('pagination');
    if(!pg) return;

    if (total <= 1) { pg.innerHTML = ''; return; }

    let html = `
        <button class="page-item arrow ${currentPage===1?'disabled':''}" onclick="goPage(${currentPage-1})">← Prev</button>
    `;

    for (let i = 1; i <= total; i++) {
        if (i === 1 || i === total || (i >= currentPage-1 && i <= currentPage+1)) {
            html += `<button class="page-item ${i===currentPage?'active':''}" onclick="goPage(${i})">${i}</button>`;
        } else if (i === currentPage-2 || i === currentPage+2) {
            html += `<span class="page-dots">···</span>`;
        }
    }

    html += `<button class="page-item arrow ${currentPage===total?'disabled':''}" onclick="goPage(${currentPage+1})">Next →</button>`;
    pg.innerHTML = html;
}

window.goPage = function(p) {
    const total = Math.ceil(displayPosts.length / PER_PAGE);
    if (p < 1 || p > total) return;
    currentPage = p;
    window.renderPosts();
    const col = document.querySelector('.posts-column');
    if(col) col.scrollIntoView({ behavior:'smooth', block:'start' });
}

