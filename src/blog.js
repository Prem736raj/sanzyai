window.filterPosts = function(btn, filter) {
    activeFilter = filter;
    document.querySelectorAll('.fchip').forEach(b => b.classList.remove('active'));
    if(btn) btn.classList.add('active');
    displayPosts = filter === 'all' ? [...allPosts] : allPosts.filter(p => p.cat === filter);
    currentPage = 1;
    window.renderPosts();
    showToast(filter === 'all' ? 'Showing all posts' : `Filtered: ${filter}`,'🏷️');
}

